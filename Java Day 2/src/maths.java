
public class maths {
	int no1,no2;
	public void add() {
		int result;
		result=no1+no2;
		System.out.println("Result: " + result);
	}
	public void subtract() {
		int result;
		result=no1-no2;
		System.out.println("Result: " + result);
	}
	public void multiply() {
		int result;
		result=no1*no2;
		System.out.println("Result: " + result);
	}
	public void divide() {
		float result;
		result=(float)no1 / no2;
		System.out.println("Result: " + result);
	}
}
